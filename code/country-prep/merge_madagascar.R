
# prep and merge data

# define files to use
meta <- tibble(file_path = all_dfs, file_name = names(all_dfs)) %>% 
  filter(str_detect(all_dfs, "/MD"))

dfs <- meta %>% 
  pull(file_path) %>% 
  map(~read_dta(., col_select = any_of(col_select))) %>% 
  map(~var_rename(., 
                  "v001" = "v001cluster",
                  "v002" = "v002hh", 
                  "v003" = "v003respondent", 
                  "v005" = "v005wgt",
                  "v007" = "v007year", 
                  "v012" = "v012age", 
                  "v025" = "v025res",
                  "v133" = "v133educ",
                  "v131" = "v131eth",
                  "v130" = "v130rel",
                  "mv001" = "v001cluster",
                  "mv002" = "v002hh", 
                  "mv003" = "v003respondent", 
                  "mv005" = "v005wgt",
                  "mv007" = "v007year", 
                  "mv012" = "v012age", 
                  "mv025" = "v025res",
                  "mv133" = "v133educ",
                  "mv131" = "v131eth",
                  "mv130" = "v130rel", 
                  verbose = FALSE)) %>% 
  map(~as_label(., v025res, v130rel, v131eth, v007year))

meta

names(dfs) <- meta %>% pull(file_name)

# harmonize year
dfs

madagascar <- bind_rows(dfs, .id = "data_name") %>% 
  mutate(country = "madagascar", 
         country_code = "MD")

madagascar %>% 
  frq(v007year)

madagascar %<>%
  mutate(year = v007year)
# gender
madagascar %<>% 
  mutate(gender = case_when(str_detect(data_name, "IR") ~ "female",
                            str_detect(data_name, "MR") ~ "male"))

# age, cohort and binned cohort
# filter by age (only working age population between 18 and 65)
madagascar %<>% 
  mutate(age = v012age,
         cohort = year-age, 
         cohort_cat = case_when(cohort < 1970 ~ "-1970", 
                                cohort >= 1970 & cohort < 1985 ~ "1970-1984",
                                cohort >= 1985  ~ "1985-"),
         cohort_cat = fct_relevel(cohort_cat, "-1970", "1970-1984", "1985-")) %>% 
  filter(age >= 18)

madagascar %>% 
  frq(cohort_cat)
# harmonize ethnicity
madagascar %<>% 
  mutate(ethnicity = as_factor(v131eth))

# harmonize religion
madagascar %>% 
  flat_table(v130rel, year)
madagascar %>% 
  flat_table(v130rel, cohort_cat)

madagascar %<>%
  mutate(religion = fct_collapse(v130rel, 
                                 "Catholic" = c("catholic"), 
                                 "Protestant" = c("protestant/flm", "protestant"),
                                 "Muslim" = c("muslim"),
                                 "Traditional" = c("traditional/animist", "traditional religion"),
                                 "Other" = c("other", "sect"), 
                                 "No religion" = c("no religion/none", "no religion")
  ))

madagascar %>% 
  flat_table(religion, cohort_cat)

# write data set
madagascar %<>% 
  select(data_name,
         country,
         country_code,
         "id_cluster" = v001cluster,
         "id_hh" = v002hh,
         "id_respondent" = v003respondent,
         "wgt" = v005wgt,
         year,
         gender,
         age,
         cohort,
         "residence" = v025res, 
         ethnicity, 
         religion, 
         "education" = v133educ)
# write data set
df <-  bind_rows(df, madagascar)
rm(meta, dfs, madagascar)


